package com.example.powerplanner;

class mojmodel {
    String cw;
    String po;
    String se;
    String pr;


    public mojmodel(String cw, String po, String se, String pr) {
        this.cw = cw;
        this.po = po;
        this.se = se;
        this.pr = pr;
    }

    public String getCw() {
        return cw;
    }

    public String getPo() {
        return po;
    }

    public String getSe() {
        return se;
    }

    public String getPr() {
        return pr;
    }
}
